package com.leetcode.medium._0019_Remove_Nth_Node_From_End_of_List;

/**
 * Definition for singly-linked list.
 */

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
